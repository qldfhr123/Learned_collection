package constructor;

public class ArrayPrint {

	
	public void print(int[]arr1) {
		for(int i : arr1)
			System.out.println(i + " ");
		System.out.println();
	}
	
	public void print(String[]arr2) {
		for(String s : arr2)
			System.out.println(s + " ");
		System.out.println();
	}
	
	public void print(double[]arr3) {
		for(double d : arr3)
			System.out.println(d + " ");
		System.out.println();
	}
	
	
}
