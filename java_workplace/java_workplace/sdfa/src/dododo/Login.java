package dododo;

public class Login {
	private static String id;
	private static String name;
	private static String gender;
	private static String ageScope; 
	private static String hobbys;
	
	public static String getId() {
		return id;
	}
	public static void setId(String id) {
		Login.id = id;
	}
	public static String getName() {
		return name;
	}
	public static void setName(String name) {
		Login.name = name;
	}
	public static String getGender() {
		return gender;
	}
	public static void setGender(String gender) {
		Login.gender = gender;
	}
	public static String getAgeScope() {
		return ageScope;
	}
	public static void setAgeScope(String ageScope) {
		Login.ageScope = ageScope;
	}
	public static String getHobbys() {
		return hobbys;
	}
	public static void setHobbys(String hobbys) {
		Login.hobbys = hobbys;
	}
	
	
}
