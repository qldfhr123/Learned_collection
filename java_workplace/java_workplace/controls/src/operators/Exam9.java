package operators;

public class Exam9 {

	public static void main(String[] args) {
		int data1 = 12;
		
		data1 = data1 <<1;
		System.out.println(data1);
			
		data1 = data1 <<1;
		System.out.println(data1);
		
		data1 = data1 <<3;
		System.out.println(data1);
		
		data1 = data1 <<1;
		System.out.println(data1);
	}

}
