package operators;

public class Exam3 {

	public static void main(String[] args) {
		double do1 = 3.1, do2 = 3.2;
		
		System.out.println("do1 <= do2 크기비교"  + (do1 <= do2));
		System.out.println("do1 >= do2 크기비교" + (do1 >= do2));
		System.out.println("do1 == do2 크기비교" + (do1 == do2));
		System.out.println("do1 != do2 크기비교" + (do1 != do2));

	}

}
